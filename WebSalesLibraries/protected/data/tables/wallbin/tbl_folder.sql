DROP TABLE IF EXISTS `tbl_folder`;
CREATE TABLE IF NOT EXISTS `tbl_folder` (
  `id` varchar(36) NOT NULL,
  `id_page` varchar(36) NOT NULL,
  `id_library` varchar(36) NOT NULL,
  `name` varchar(256) NOT NULL,
  `column_order` int(11) NOT NULL,
  `row_order` int(11) NOT NULL,
  `border_color` varchar(8) NOT NULL,
  `window_back_color` varchar(8) NOT NULL,
  `window_fore_color` varchar(8) NOT NULL,
  `header_back_color` varchar(8) NOT NULL,
  `header_fore_color` varchar(8) NOT NULL,
  `window_font_name` varchar(64) NOT NULL,
  `window_font_size` int(11) NOT NULL,
  `window_font_bold` tinyint(1) NOT NULL,
  `window_font_italic` tinyint(1) NOT NULL,
  `header_font_name` varchar(64) NOT NULL,
  `header_font_size` int(11) NOT NULL,
  `header_font_bold` tinyint(1) NOT NULL,
  `header_font_italic` tinyint(1) NOT NULL,
  `header_alignment` varchar(64) NOT NULL,
  `enable_widget` tinyint(1) NOT NULL,
  `widget` mediumblob,
  `id_banner` varchar(36) NOT NULL,
  `folder_settings` longtext,
  `widget_settings` longtext,
  `banner_settings` longtext,
  `date_add` datetime NULL,
  `date_modify` datetime NULL,
  PRIMARY KEY (`id`),
  KEY `id_page` (`id_page`),
  KEY `id_library` (`id_library`)
) ENGINE=MyISAM  DEFAULT CHARSET=latin1 AUTO_INCREMENT=55 ;